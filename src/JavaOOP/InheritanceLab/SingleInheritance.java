import java.util.Stack;

public class SingleInheritance {
    public static void main(String[] args) {
        Stack<Object> demo= new Stack<>();
        System.out.println(demo.empty());
        demo.push("asd");
        demo.push("sdf");
        demo.push("dfg");
        demo.push("fgh");
        System.out.println(demo.empty());
    }



}
